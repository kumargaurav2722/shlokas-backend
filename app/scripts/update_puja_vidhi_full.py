def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Update Puja Vidhi content")
    parser.add_argument("--dry-run", action="store_true")
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    db = SessionLocal()
    try:
        updated = 0
        for deity, payload in CONTENT.items():
            record = db.query(PujaVidhi).filter(PujaVidhi.deity == deity).first()
            if not record:
                continue
            record.title = payload["title"]
            record.content = payload["content"]
            record.content_type = "text"
            record.source_url = "https://en.wikipedia.org/wiki/Puja_(Hinduism)"
            record.license = "CC BY-SA 4.0"
            record.attribution = "Compiled from common puja steps; deity-specific offerings vary by tradition."
            updated += 1

        if args.dry_run:
            db.rollback()
        else:
            db.commit()
        print(f"Updated {updated} puja vidhi records")
    finally:
        db.close()


if __name__ == "__main__":
    main()
